           PreparedStatement pps = cn.prepareStatement("INSERT INTO datos(Nombre,Apellido,Edad,Correo) VALUES(?,?,?,?)");
            pps.setString(1,txtnombre.getText());
              pps.setString(2,txtapellido.getText());
                pps.setString(3,txtedad.getText());
